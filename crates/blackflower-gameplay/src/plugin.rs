use std::path::Path;
use wasmtime::component::{Component, HasSelf, Linker, ResourceTable};
use wasmtime::{Config, Engine, Store};
use wasmtime_wasi::{WasiCtx, WasiCtxBuilder, WasiCtxView, WasiView};

wasmtime::component::bindgen!({
    path: "../../wit/game-plugin.wit",
    world: "game-plugin",
});

/// Raw prop list as used by the WASM interface — `(id, raw bytes)`.
pub type PropList = Vec<(u16, Vec<u8>)>;

/// Outcome of a hit: the target's updated props (merged by id) plus whether the
/// plugin declared the target dead. The engine never reads HP — it respawns the
/// target only when `respawn` is set.
pub struct HitOutcome {
    pub props: PropList,
    pub respawn: bool,
}

/// A sound the plugin published via the `play-sound` import, with the world
/// position to play it at. The `sound` id is opaque — the client maps it to an
/// asset.
pub struct PlaySound {
    pub sound: String,
    pub position: [f32; 3],
}

struct PluginState {
    ctx: WasiCtx,
    table: ResourceTable,
    /// The event bus: sounds the guest published via `play-sound`. The engine
    /// drains it once per tick (`Plugin::drain_events`).
    events: Vec<PlaySound>,
}

impl WasiView for PluginState {
    fn ctx(&mut self) -> WasiCtxView<'_> {
        WasiCtxView {
            ctx: &mut self.ctx,
            table: &mut self.table,
        }
    }
}

impl GamePluginImports for PluginState {
    fn play_sound(&mut self, sound: String, position: Vec3) {
        self.events.push(PlaySound {
            sound,
            position: [position.x, position.y, position.z],
        });
    }
}

/// Host-side WASM plugin. Loads a component and exposes engine hooks.
/// `GamePlugin` is generated by `bindgen!`; this struct owns the store.
pub struct Plugin {
    store: Store<PluginState>,
    bindings: GamePlugin,
}

impl Plugin {
    pub fn load(path: &Path) -> anyhow::Result<Self> {
        let mut config = Config::new();
        config.wasm_component_model(true);
        let engine =
            Engine::new(&config).map_err(|e| anyhow::anyhow!("creating wasmtime engine: {e}"))?;

        let component = Component::from_file(&engine, path)
            .map_err(|e| anyhow::anyhow!("loading plugin from {}: {e}", path.display()))?;

        let mut linker: Linker<PluginState> = Linker::new(&engine);
        wasmtime_wasi::p2::add_to_linker_sync(&mut linker)
            .map_err(|e| anyhow::anyhow!("adding WASI to plugin linker: {e}"))?;
        GamePlugin::add_to_linker::<PluginState, HasSelf<PluginState>>(
            &mut linker,
            |state: &mut PluginState| state,
        )
        .map_err(|e| anyhow::anyhow!("adding game-plugin imports to linker: {e}"))?;

        let state = PluginState {
            ctx: WasiCtxBuilder::new().build(),
            table: ResourceTable::new(),
            events: Vec::new(),
        };
        let mut store = Store::new(&engine, state);

        let bindings = GamePlugin::instantiate(&mut store, &component, &linker)
            .map_err(|e| anyhow::anyhow!("instantiating plugin component: {e}"))?;

        Ok(Self { store, bindings })
    }

    /// Pick which map spawn point a player spawns at. `candidates` are
    /// `(origin, angle)` pairs from the map; returns the chosen index,
    /// clamped into range against `candidates.len()`.
    pub fn select_spawn(&mut self, candidates: &[([f32; 3], f32)]) -> anyhow::Result<usize> {
        if candidates.is_empty() {
            return Ok(0);
        }
        let wit: Vec<SpawnPoint> = candidates
            .iter()
            .map(|&(origin, angle)| SpawnPoint {
                origin: Vec3 {
                    x: origin[0],
                    y: origin[1],
                    z: origin[2],
                },
                angle,
            })
            .collect();
        let idx = self
            .bindings
            .call_select_spawn(&mut self.store, &wit)
            .map_err(|e| anyhow::anyhow!("plugin select_spawn: {e}"))?;
        Ok((idx as usize) % candidates.len())
    }

    /// Called when a new player entity is spawned.
    pub fn on_spawn(&mut self) -> anyhow::Result<PropList> {
        self.bindings
            .call_on_spawn(&mut self.store)
            .map_err(|e| anyhow::anyhow!("plugin on_spawn: {e}"))
    }

    /// Called when a player fires (at `shooter`). The plugin publishes any fire
    /// sound to the bus via `play-sound`; drain it with [`Plugin::drain_events`].
    pub fn on_fire(&mut self, shooter: [f32; 3]) -> anyhow::Result<()> {
        self.bindings
            .call_on_fire(&mut self.store, to_vec3(shooter))
            .map_err(|e| anyhow::anyhow!("plugin on_fire: {e}"))
    }

    /// Called when a hitscan ray confirms a hit on the target (at `target`). Any
    /// hit/death sound is published to the bus; this returns only the
    /// engine-actionable outcome (props + respawn).
    pub fn on_hit(
        &mut self,
        target_props: &PropList,
        target: [f32; 3],
    ) -> anyhow::Result<HitOutcome> {
        let result = self
            .bindings
            .call_on_hit(&mut self.store, target_props, to_vec3(target))
            .map_err(|e| anyhow::anyhow!("plugin on_hit: {e}"))?;
        Ok(HitOutcome {
            props: result.props,
            respawn: result.respawn,
        })
    }

    /// Drain the events the plugin published to the bus since the last drain.
    /// The engine relays these once per tick.
    pub fn drain_events(&mut self) -> Vec<PlaySound> {
        std::mem::take(&mut self.store.data_mut().events)
    }

    /// Serialize the plugin's internal state to opaque bytes (for hot-reload
    /// migration). The engine never interprets the bytes.
    pub fn save_state(&mut self) -> anyhow::Result<Vec<u8>> {
        self.bindings
            .call_save_state(&mut self.store)
            .map_err(|e| anyhow::anyhow!("plugin save_state: {e}"))
    }

    /// Restore internal state from bytes previously produced by `save_state`
    /// (the plugin migrates older formats). Empty means a fresh start.
    pub fn load_state(&mut self, state: &[u8]) -> anyhow::Result<()> {
        self.bindings
            .call_load_state(&mut self.store, state)
            .map_err(|e| anyhow::anyhow!("plugin load_state: {e}"))
    }
}

const fn to_vec3(a: [f32; 3]) -> Vec3 {
    Vec3 {
        x: a[0],
        y: a[1],
        z: a[2],
    }
}
